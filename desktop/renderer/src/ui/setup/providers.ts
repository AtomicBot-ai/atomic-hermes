export function resolveProviderIconUrl(providerId: string): string | undefined {
  const filename = PROVIDER_ICONS[providerId];
  if (!filename) return undefined;
  return new URL(
    `../../../../assets/ai-providers/${filename}`,
    import.meta.url,
  ).toString();
}

